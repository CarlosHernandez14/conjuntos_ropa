    private void cargarPublicacionDefault() {

        ArrayList<Publicacion> publicaciones = (ArrayList<Publicacion>) WebServiceManager.obtenerPublicaciones();

        // Filtramos por la publicacion default de ese usuario
        Publicacion publicacionDefault = publicaciones.stream()
                .filter(publicacion -> publicacion.getIdUsuario() == this.usuarioLogueado.getIdUsuario() && publicacion.getTitulo().equals("DEFAULT"))
                .findFirst()
                .orElse(null);

        if (publicacionDefault != null) {
            this.publicacion = publicacionDefault;
        } else {
            JOptionPane.showMessageDialog(null, "No se encontro la publicacion default del usuario");
            this.dispose();
        }

    }

