    private void btnSelectFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectFotoActionPerformed
        // TODO add your handling code here:
        
        JnaFileChooser fileChooser = new JnaFileChooser();
        if (fileChooser.showOpenDialog(this)) {
            try {
                String imagePath = fileChooser.getSelectedFile().toPath().toString();
                File foto = new File(imagePath);

                this.fotoCargadaBytes = Files.readAllBytes(foto.toPath());
                // Cargamos la foto
                this.panelFoto.setIcon(new ImageIcon(this.fotoCargadaBytes));

                this.panelFoto.revalidate();
                this.panelFoto.repaint();

            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Error al cargar la foto seleccionada");
                System.out.println("Error al cargar la foto: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSelectFotoActionPerformed

    private void btnAddPrendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPrendaActionPerformed
        // TODO add your handling code here:

        String nombre = this.fieldNombre.getText();
        String tipo = this.fieldTipo.getText();

        // Verificamos que los campos no esten vacios
        if (nombre.isEmpty() || tipo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor llena todos los campos");
            return;
        }

        // Guardamos la prenda
        Prenda prenda = new Prenda(this.publicacion.getIdPublicacion(), nombre, tipo);
        
        if (this.fotoCargadaBytes != null) {
            prenda.setFoto(this.fotoCargadaBytes);
        }

        // Guardamos la prenda
        int idPrenda = WebServiceManager.guardarPrenda(prenda);

        if (idPrenda != -1) {
            JOptionPane.showMessageDialog(null, "Prenda guardada correctamente");
            if (this.homeUsuarios != null) this.homeUsuarios.actualizarPrendas();
            if (this.prendasForm != null) this.prendasForm.actualizarPrendas();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error al guardar la prenda");
        }

    }//GEN-LAST:event_btnAddPrendaActionPerformed

