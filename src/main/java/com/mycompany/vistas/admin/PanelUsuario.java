    private void btnBloqeuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBloqeuarActionPerformed
        // TODO add your handling code here:
        
        // Actualizamos al usuario

        if (this.usuario.isBloqueado()) {
            this.usuario.setBloqueado(false);

            this.usuario.setContrasena(null);
            
            int idUsuario = WebServiceManager.actualizarUsuario(usuario);

            if (idUsuario != -1) {
                // NOTIFICAR QUE SE ACTUALIZÓ CORRECTAMENTE
                // Actualizar el panel
                cargarDatos();
            } else {
                // NOTIFICAR QUE HUBO UN ERROR
                JOptionPane.showMessageDialog(null, "Error al actualizar el usuario");
            }

        } else {
            this.usuario.setBloqueado(true);

            int idUsuario = WebServiceManager.actualizarUsuario(usuario);

            if (idUsuario != -1) {
                // NOTIFICAR QUE SE ACTUALIZÓ CORRECTAMENTE
                // Actualizar el panel
                cargarDatos();
            } else {
                // NOTIFICAR QUE HUBO UN ERROR
                JOptionPane.showMessageDialog(null, "Error al actualizar el usuario");
            }
        }

    }//GEN-LAST:event_btnBloqeuarActionPerformed


