    private void cargarDatos() {
        this.labelCorreo.setText(this.usuario.getCorreo());
        this.labelNombre.setText(this.usuario.getNombre());
        
        // Si el usuario está bloqueado, se cambia el color del panel y se cambia el texto del botón
        if (this.usuario.isBloqueado()) {
            this.setBackground(new java.awt.Color(255, 204, 204));
            this.btnBloqeuar.setText("Desbloquear");
        } else {
            this.setBackground(new java.awt.Color(242, 242, 242));
            this.btnBloqeuar.setText("Bloquear");
        }

        this.revalidate();
        this.repaint();
    }

