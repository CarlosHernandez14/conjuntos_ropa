    private void itemGustaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemGustaActionPerformed
        // TODO add your handling code here:
        // Guardamos la reaccion de gusta
        Reaccion reaccion = new Reaccion(this.publicacion.getIdPublicacion(), this.usuarioLogueado.getIdUsuario(), TipoReaccion.GUSTA);
        
        if (this.reaccion != null) this.reaccion.setTipo(TipoReaccion.GUSTA);

        int idReaccion = this.reaccion != null ? WebServiceManager.actualizarReaccion(this.reaccion) : WebServiceManager.guardarReaccion(reaccion);

        if (idReaccion == -1) {
            JOptionPane.showMessageDialog(null, "No se pudo guardar la reaccion, intentalo mas tarde");
            return;
        }

        // Cambiamos el icono
        this.btnReaction.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/icon-gusta.png")));
    }//GEN-LAST:event_itemGustaActionPerformed

