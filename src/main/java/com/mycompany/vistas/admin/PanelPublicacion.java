    private void cargarDatos() {
        this.labelTitulo.setText(publicacion.getTitulo());
        this.textAreaDecription.setText(publicacion.getDescripcion());
        String foto64 = Base64.getEncoder().encodeToString(publicacion.getFoto());

        if (!foto64.isEmpty()) {
            this.panelFoto.setIcon(new ImageIcon(this.publicacion.getFoto()));
        }

        // Vemos si ya tiene reaccion
        Reaccion reaccionObtenida = this.publicacion.getReacciones().stream()
                .filter(r -> r.getIdUsuario() == this.usuarioLogueado.getIdUsuario())
                .findFirst()
                .orElse(null);

        if (reaccionObtenida != null) {
            this.reaccion = reaccionObtenida;
            // Asignamos el icon segun el tipo
            switch (reaccionObtenida.getTipo()) {
                case GUSTA -> this.btnReaction.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/icon-gusta.png")));
                case ENCANTA -> this.btnReaction.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/icon-encanta.png")));
                case DIVIERTE -> this.btnReaction.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/icon-divierte.png")));
                case SORPRENDE -> this.btnReaction.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/icon-sorprende.png")));
                default -> {
                }
            }
        }

        // Consultamos al usuario
        Usuario usuario = WebServiceManager.obtenerUsuarioPorId(publicacion.getIdUsuario());

        if (usuario == null) {
            JOptionPane.showMessageDialog(null, "No se pudo obtener al usuario de la publicacion");
            return;
        }

        this.labelUsername.setText(usuario.getNombre());

        //Cargamos los coomentarios
        cargarComentarios();
        cargarPermisosBotones();

    }

    public void cargarComentarios() {
        this.btnComentarios.setText("Comentarios (" + this.publicacion.getComentarios().size() + ")");
    }
    
